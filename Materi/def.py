def luas_persegi_panjang(panjang, lebar):
    print(">panjang: ", panjang)
    print(">lebar:", lebar)
    rumus = panjang * lebar 
    print("luas persegi panjang=", rumus) 

luas_persegi_panjang(45, 40)

def luas_segitiga(alas, tinggi):
    print("> alas:", alas)
    print("> tinggi:", tinggi)
    luas = 1.5 * alas * tinggi
    print("luas segitiga =", luas)

